Package: RedshiftRDBA
Title: Tools for easier DBA tasks for Amazon Redshift
Description: A set of useful tools for the administration of Amazon Redshift. You can use it
to check the design of the tables (data types, encoding, distribution and sort keys), the
loading of the data (COPY, file size, sorted parts), queries (speed, queues, explain)
